const SOLAR_TESTER_VECTOR_STOKES = Dict{Symbol, Array{Float64, 4}}(
    :I => SOLAR_TESTER_VECTOR_I,
    :Q => SOLAR_TESTER_VECTOR_Q,
    :U => SOLAR_TESTER_VECTOR_U,
)
""")
    end
    @info "wrote" path
end

