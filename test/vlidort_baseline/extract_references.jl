function main()
    isdir(VLIDORT_DIR) || error("PyVLIDORT not found at $VLIDORT_DIR")

    @info "extracting Siewert 2000 IIA"
    siewert_src = joinpath(VLIDORT_DIR, "vlidort_v_test", "V2p8p3_Siewert2000_validation.f90")
    siewert_res = joinpath(VLIDORT_DIR, "vlidort_v_test", "saved_results", "gfortran",
                           "results_Siewert2000_validation.all")
    rows = extract_siewert_greek(siewert_src)
    emit_siewert_greek(rows)
    tables = extract_siewert_tables(siewert_res)
    @info "parsed Siewert tables" ids=sort!(collect(keys(tables)))
    emit_siewert_tables(tables)

    @info "extracting solar_tester (scalar)"
    atmos_path = joinpath(VLIDORT_DIR, "vlidort_s_test", "input_atmos.dat")
    truth_path = joinpath(VLIDORT_DIR, "vlidort_s_test", "saved_results", "gfortran",
                          "results_solar_tester.all")
    atmos = extract_solar_tester_atmosphere(atmos_path)
    emit_solar_tester_atmosphere(atmos)
    truth = extract_solar_tester_stokes(truth_path)
    @info "parsed solar_tester truth" tau_levels=truth.tau_levels size=size(truth.stokes)
    emit_solar_tester_truth(truth.stokes, truth.tau_levels)

    @info "extracting solar_tester (vector, Stokes-3)"
    moms_path = joinpath(VLIDORT_DIR, "vlidort_v_test", "ProblemIII.Moms")
    vec_truth_path = joinpath(VLIDORT_DIR, "vlidort_v_test", "saved_results",
                              "gfortran", "nstokes3", "results_solar_tester_IQU0.all")
    moms = extract_problemIII_moms(moms_path)
    @info "parsed Problem III aerosol moments" n=length(moms.a1)
    emit_problemIII_moms(moms)
    vec_truth, vec_levels = extract_solar_tester_vector_truth(vec_truth_path)
    @info "parsed solar_tester vector truth" tau_levels=vec_levels stokes=collect(keys(vec_truth))
    emit_solar_tester_vector_truth(vec_truth, vec_levels)

    @info "done"
end

main()
