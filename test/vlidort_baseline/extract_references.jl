# ---- Siewert 2000 Problem IIA ------------------------------------------------

"Parse `DATA (PROBLEM_IIA(k,L), L = 0,11)/ ... /` blocks from the Fortran source."
function extract_siewert_greek(fortran_path::AbstractString)
    src = read(fortran_path, String)
    rows = Vector{Vector{Float64}}(undef, 6)
    for k in 1:6
        m = match(Regex("DATA \\(PROBLEM_IIA\\($k,L\\),L = 0,11\\)/(.*?)/", "s"), src)
        m === nothing && error("PROBLEM_IIA row $k not found in $fortran_path")
        body = m.captures[1]
        nums = [parse(Float64, replace(s, r"[Dd]" => "e"))
                for s in eachmatch(r"[-+]?\d+\.\d+(?:[DdEe][-+]?\d+)?", body) |>
                    collect .|> m -> m.match]
        length(nums) == 12 || error("PROBLEM_IIA row $k: expected 12 moments, got $(length(nums))")
        rows[k] = nums
    end
    return rows
end

"Parse fixed-width tables from results_Siewert2000_validation.all.
Each table is 22 rows × (1 cosine + 7 τ-level columns). Returns
`Dict{Int, NamedTuple{(:cosines, :values), Tuple{Vector{Float64}, Matrix{Float64}}}}`."
function extract_siewert_tables(results_path::AbstractString)
    tables = Dict{Int, NamedTuple{(:cosines, :values), Tuple{Vector{Float64}, Matrix{Float64}}}}()
    lines = readlines(results_path)
    current_id = 0
    cosines = Float64[]
    rows = Vector{Vector{Float64}}()
    function flush_current()
        if current_id > 0 && !isempty(cosines)
            mat = reduce(hcat, rows) |> permutedims  # rows × cols
            tables[current_id] = (cosines=copy(cosines), values=mat)
        end
    end
    for raw in lines
        m = match(r"^\s*Table\s+(\d+)\s*$", raw)
        if m !== nothing
            flush_current()
            current_id = parse(Int, m.captures[1])
            empty!(cosines); empty!(rows)
            continue
        end
        nums = [parse(Float64, s.match)
                for s in eachmatch(r"-?\d+\.\d+(?:E[-+]?\d+)?", raw)]
        if !isempty(nums) && current_id > 0
            length(nums) >= 8 || continue   # cosine + 7 τ-levels
            push!(cosines, nums[1])
            push!(rows, nums[2:end])
        end
    end
    flush_current()
    return tables
end

