# ---- solar_tester (vector, Stokes-3) — Problem III aerosol ------------------

"Parse `ProblemIII.Moms` (Mie F-matrix expansion, 6 elements per L).
File layout: 5 lines of header/blank/column-header, then rows
`L  a1  b1  a2  a3  b2  a4` starting at line 6. The driver applies a cutoff:
keeps moments while a1 > 1e-6, plus the first below-cutoff moment
(implicit in V2p8p3_solar_tester.f90:294-322). Returns a NamedTuple
with fields `(:a1, :b1, :a2, :a3, :b2, :a4)`, each a vector indexed
L = 0..ngreek_moments_input."
function extract_problemIII_moms(path::AbstractString; cutoff::Float64 = 1.0e-6,
                                  L_max::Int = 106)
    lines = readlines(path)
    columns = (Float64[], Float64[], Float64[], Float64[], Float64[], Float64[])
    lcheck = 0
    L = -1
    # Data rows start at line 6 (after 5 header/blank lines).
    for raw in lines[6:end]
        nums = try
            [parse(Float64, s) for s in split(raw)]
        catch
            continue
        end
        length(nums) == 7 || continue
        amoms = nums[2:end]   # a1, b1, a2, a3, b2, a4
        if abs(amoms[1]) > cutoff
            L += 1
            for k in 1:6
                push!(columns[k], amoms[k])
            end
        else
            lcheck += 1
            if lcheck == 1
                L += 1
                for k in 1:6
                    push!(columns[k], amoms[k])
                end
            end
        end
        (L >= L_max || lcheck >= 2) && break
    end
    return (a1=columns[1], b1=columns[2], a2=columns[3],
            a3=columns[4], b2=columns[5], a4=columns[6])
end

"Parse the multi-block STOKES-{I,Q,U} structure from
`results_solar_tester_IQU0.all`. Each block has the same geom × level ×
direction × task layout as scalar solar_tester. Returns a 4-D array
`stokes[geom, level, dir, task]` per stokes component, packaged in a
Dict keyed by :I, :Q, :U."
function extract_solar_tester_vector_truth(path::AbstractString;
                                            n_geometries::Int = 36,
                                            n_levels::Int = 5,
                                            n_tasks::Int = 6)
    lines = readlines(path)
    out = Dict{Symbol, Array{Float64, 4}}()
    current = nothing
    levels = Float64[]
    block_arr = nothing
    re_block = r"^\s*STOKES-([IQU])"
    re_row = r"^\s*(\d+)\s+(Upwelling|Dnwelling)\s+@\s*(-?\d+\.\d+)\s+(.+)$"
    for raw in lines
        m_block = match(re_block, raw)
        if m_block !== nothing
            sym = Symbol(m_block.captures[1])
            current = sym
            block_arr = zeros(Float64, n_geometries, n_levels, 2, n_tasks)
            empty!(levels)
            out[sym] = block_arr
            continue
        end
        current === nothing && continue
        m_row = match(re_row, raw)
        m_row === nothing && continue
        geom = parse(Int, m_row.captures[1])
        dir  = m_row.captures[2] == "Upwelling" ? 1 : 2
        lvl  = parse(Float64, m_row.captures[3])
        if dir == 1 && geom == 1
            lvl in levels || push!(levels, lvl)
        end
        nums = [parse(Float64, s.match)
                for s in eachmatch(r"-?\d+\.\d+E[-+]?\d+", m_row.captures[4])]
        length(nums) >= n_tasks || continue
        ilvl = findfirst(==(lvl), levels)
        ilvl === nothing && (push!(levels, lvl); ilvl = length(levels))
        for t in 1:n_tasks
            block_arr[geom, ilvl, dir, t] = nums[t]
        end
    end
    return out, levels
end

