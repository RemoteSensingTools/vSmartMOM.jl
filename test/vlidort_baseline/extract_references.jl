# ---- solar_tester (scalar) atmosphere + truth -------------------------------

"Parse `vlidort_s_test/input_atmos.dat`. Layout:
  row 1: raymoms[0, n=1..23]   (all 1.0 for Rayleigh L=0)
  row 2: raymoms[1, n=1..23]   (all 0.0 for Rayleigh L=1)
  row 3: raymoms[2, n=1..23]   (Rayleigh L=2 — depol-dependent, here 0.4920)
  rows 4..26: per-layer (n, height_km, molext, molomg, kd1, kd2, kd3, kd4)
Returns a NamedTuple with the parsed fields."
function extract_solar_tester_atmosphere(path::AbstractString)
    lines = readlines(path)
    @assert length(lines) >= 26 "expected ≥26 lines, got $(length(lines))"
    parse_row(line) = [parse(Float64, s) for s in split(line)]
    raymoms = Matrix{Float64}(undef, 3, 23)
    for k in 1:3
        nums = parse_row(lines[k])
        @assert length(nums) == 24 "row $k: expected 24 cols (idx + 23 layers), got $(length(nums))"
        raymoms[k, :] = nums[2:end]
    end
    height_km = Vector{Float64}(undef, 23)
    molext    = Vector{Float64}(undef, 23)
    molomg    = Vector{Float64}(undef, 23)
    for n in 1:23
        nums = parse_row(lines[3 + n])
        @assert length(nums) >= 4 "layer row $n: expected ≥4 cols"
        @assert Int(nums[1]) == n "layer index mismatch at row $(3+n)"
        height_km[n] = nums[2]
        molext[n]    = nums[3]
        molomg[n]    = nums[4]
    end
    return (height_km=height_km, molext=molext, molomg=molomg, raymoms=raymoms)
end

"Parse the STOKES-I block from `results_solar_tester.all`.

Returns a 3-D array `stokes[geom, level, dir, task]` of size
`(36, 5, 2, 6)`, plus the τ-level vector and direction labels.
Direction order: 1=Upwelling, 2=Dnwelling. Task order: 1..6."
function extract_solar_tester_stokes(path::AbstractString;
                                     n_geometries::Int = 36,
                                     n_levels::Int = 5,
                                     n_tasks::Int = 6)
    lines = readlines(path)
    stokes = zeros(Float64, n_geometries, n_levels, 2, n_tasks)
    levels = Float64[]
    re = r"^\s*(\d+)\s+(Upwelling|Dnwelling)\s+@\s*(-?\d+\.\d+)\s+(.+)$"
    for raw in lines
        m = match(re, raw)
        m === nothing && continue
        geom = parse(Int, m.captures[1])
        dir  = m.captures[2] == "Upwelling" ? 1 : 2
        lvl  = parse(Float64, m.captures[3])
        if dir == 1 && geom == 1
            lvl in levels || push!(levels, lvl)
        end
        nums = [parse(Float64, s.match)
                for s in eachmatch(r"-?\d+\.\d+E[-+]?\d+", m.captures[4])]
        length(nums) >= n_tasks || continue
        # find the level index
        ilvl = findfirst(==(lvl), levels)
        ilvl === nothing && (push!(levels, lvl); ilvl = length(levels))
        for t in 1:n_tasks
            stokes[geom, ilvl, dir, t] = nums[t]
        end
    end
    @assert length(levels) == n_levels "expected $n_levels τ-levels, got $(length(levels))"
    return (stokes=stokes, tau_levels=levels)
end

